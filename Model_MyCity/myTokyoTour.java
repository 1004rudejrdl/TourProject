package model;

public class myTokyoTour {
	private String myTokyoTourID;
	private String myTokyoTourName;
	
	public myTokyoTour(String myTokyoTourID, String myTokyoTourName) {
		super();
		this.myTokyoTourID = myTokyoTourID;
		this.myTokyoTourName = myTokyoTourName;
	}

	public String getMyTokyoTourID() {
		return myTokyoTourID;
	}

	public void setMyTokyoTourID(String myTokyoTourID) {
		this.myTokyoTourID = myTokyoTourID;
	}

	public String getMyTokyoTourName() {
		return myTokyoTourName;
	}

	public void setMyTokyoTourName(String myTokyoTourName) {
		this.myTokyoTourName = myTokyoTourName;
	}
	

}
