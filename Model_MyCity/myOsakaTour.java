package model;

public class myOsakaTour {
	private String myOsakaTourID;
	private String myOsakaTourName;
	
	public myOsakaTour(String myOsakaTourID, String myOsakaTourName) {
		super();
		this.myOsakaTourID = myOsakaTourID;
		this.myOsakaTourName = myOsakaTourName;
	}

	public String getMyOsakaTourID() {
		return myOsakaTourID;
	}

	public void setMyOsakaTourID(String myOsakaTourID) {
		this.myOsakaTourID = myOsakaTourID;
	}

	public String getMyOsakaTourName() {
		return myOsakaTourName;
	}

	public void setMyOsakaTourName(String myOsakaTourName) {
		this.myOsakaTourName = myOsakaTourName;
	}
		

}
