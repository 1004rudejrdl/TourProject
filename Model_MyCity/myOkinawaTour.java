package model;

public class myOkinawaTour {
	private String myOkinawaTourID;
	private String myOkinawaTourName;
	
	public myOkinawaTour(String myOkinawaTourID, String myOkinawaTourName) {
		super();
		this.myOkinawaTourID = myOkinawaTourID;
		this.myOkinawaTourName = myOkinawaTourName;
	}

	public String getMyOkinawaTourID() {
		return myOkinawaTourID;
	}

	public void setMyOkinawaTourID(String myOkinawaTourID) {
		this.myOkinawaTourID = myOkinawaTourID;
	}

	public String getMyOkinawaTourName() {
		return myOkinawaTourName;
	}

	public void setMyOkinawaTourName(String myOkinawaTourName) {
		this.myOkinawaTourName = myOkinawaTourName;
	}
	
	

}
