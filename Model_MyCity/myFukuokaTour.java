package model;

public class myFukuokaTour {
	private String myFukuokaTourID;
	private String myFukuokaTourName;
	
	public myFukuokaTour(String myFukuokaTourID, String myFukuokaTourName) {
		super();
		this.myFukuokaTourID = myFukuokaTourID;
		this.myFukuokaTourName = myFukuokaTourName;
	}

	public String getMyFukuokaTourID() {
		return myFukuokaTourID;
	}

	public void setMyFukuokaTourID(String myFukuokaTourID) {
		this.myFukuokaTourID = myFukuokaTourID;
	}

	public String getMyFukuokaTourName() {
		return myFukuokaTourName;
	}

	public void setMyFukuokaTourName(String myFukuokaTourName) {
		this.myFukuokaTourName = myFukuokaTourName;
	}
	
	


}
