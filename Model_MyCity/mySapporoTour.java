package model;

public class mySapporoTour {
	private String mySapporoTourID;
	private String mySapporoTourName;
	
	public mySapporoTour(String mySapporoTourID, String mySapporoTourName) {
		super();
		this.mySapporoTourID = mySapporoTourID;
		this.mySapporoTourName = mySapporoTourName;
	}

	public String getMySapporoTourID() {
		return mySapporoTourID;
	}

	public void setMySapporoTourID(String mySapporoTourID) {
		this.mySapporoTourID = mySapporoTourID;
	}

	public String getMySapporoTourName() {
		return mySapporoTourName;
	}

	public void setMySapporoTourName(String mySapporoTourName) {
		this.mySapporoTourName = mySapporoTourName;
	}
	
	
	

		

}
